/*
* 三根针，A,B,C，将N个盘子从A移动到C，使用递归的方法。
*/
#include<iostream>
using namespace std;
void move(char src, char dest) {
	cout << src << "-->" << dest << endl;
}
void hanoi(int n, char src, char medium, char dest) {
	if (n == 1) {
		move(src, dest);
	}
	else
	{
		hanoi(n - 1, src, dest, medium);
		move(src, dest);
		hanoi(n - 1, medium, src, dest);
	}
}
int main() {
	int m;
	cout << "输入盘子的个数" << endl;
	cin >> m;
	cout << "步骤" << m << endl;
	hanoi(m, 'A', 'B', 'C');
	return 0;
}

